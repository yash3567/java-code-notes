class typeconversion {

    public static void main(String[] args) {

        // byte b = 126;
        int a = 257;
        byte k = (byte) a;
        System.out.println(k);

        float f = 7.5f;
        int t = (int) f;
        System.out.println(t);

        byte u = 20;
        byte b = 50;
        int result = u * b;
        System.out.println(result);

    }
}
