class ifelseif {
    public static void main(String args[]) {

        int a = 5000;
        int b = 1000;
        int c = 150;

        if (a > b && a > c)

            System.out.println(a);

        else if (b > c)
            System.out.println(b);

        else
            System.out.println(c);

    }
}